                            <div class="col-md-6 col-sm-12 col-xs-12 sm-margin-five-top">
                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                Dios Hijo</h5>
                                <p></p>

                                <p class="dropcap text-justify">Dios Hijo, Jesucristo, es quién siendo verdadero y eterno Dios, se encarnó como Hombre,
                                nació virginalmente por obra del Espíritu Santo, asumió plena naturaleza humana, pero
                                sin pecado. Murió en la cruz, resucitó y ascendió corporalmente a los cielos, desde donde
                                volverá con poder y gran gloria. Como verdadero Dios y verdadero Hombre, es el único
                                mediador entre Dios y la humanidad. Sus dos naturalezas están presentes en El de manera
                                inconfundible, inalterable, indivisible e inseparable. Jesucristo cumple a cabalidad los tres
                                oficios regentes de sacerdote, profeta y rey, siendo Señor y cabeza de la Iglesia y
                                autoridad suprema sobre toda la creación.</p>
