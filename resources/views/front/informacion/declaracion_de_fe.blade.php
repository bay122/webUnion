                            <div class="col-md-6 col-sm-12 col-xs-12 sm-margin-five-top">
                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                Dios Trino</h5>
                                <p></p>

                                <p class="dropcap text-justify">Hay un solo Dios verdadero y éste subsiste en tres personas, existiendo eternamente en el
                                Padre, el Hijo y el Espíritu Santo. Cada persona de la Trinidad es plenamente Dios, siendo
                                una sola esencia divina e inseparable. Dios hace todo para Su gloria y todo fue creado por
                                y para El. Dios es infinito, independiente, inmutable e indivisible en todos sus atributos.
                                Las tres personas de la Trinidad comparten los mismos atributos, siendo iguales en
                                divinidad, poder, gloria y honra, aunque distintos en sus roles dentro de la relación intra-
                                trinitaria y con su creación.</p>
                            </div>
