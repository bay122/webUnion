                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                La Iglesia</h5>
                                <p></p>

                                <p class="dropcap text-justify">La Iglesia es la comunidad de creyentes de toda lengua, pueblo, tribu y nación, llamadas
                                por el Padre, para ser parte de su pueblo y obedecer a Cristo, por medio del Espíritu
                                Santo. Cristo es la cabeza y el esposo de la iglesia, que es su cuerpo, siendo ésta amada,
                                sustentada y cuidada por El. Dios invita a su Iglesia a unirse a su misión, dando a conocer
                                su gloria en medio de las naciones, haciendo discípulos de Jesucristo, bautizándoles y
                                enseñándoles a poner en práctica toda la Palabra de Dios. La iglesia está llamada a vivir en
                                santidad, siendo extranjera y peregrina en este mundo, velando y anhelando la venida de
                                Jesús.</p>
                            </div>

                            <div class="col-md-6 col-sm-12 col-xs-12 sm-margin-five-top">
                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                La Oración</h5>
                                <p></p>

                                <p class="dropcap text-justify">La oración es la comunicación privilegiada que los hijos de Dios tienen con su Padre
                                celestial. Es una relación viva con Dios, quién oye y responde las oraciones que están
                                conforme a su voluntad y traen gloria a su Nombre. Mientras permanecemos en El,
                                nuestros corazones y peticiones se van ajustando a su voluntad. Al orar demostramos
                                dependencia de Dios, en desmedro de la autosuficiencia, declarando que el poder y la
                                capacidad residen únicamente en Cristo.</p>

