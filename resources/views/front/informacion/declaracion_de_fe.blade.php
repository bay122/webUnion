                                La Familia</h5>
                                <p></p>

                                <p class="dropcap text-justify">Dios estableció el matrimonio como base fundamental de la familia. El matrimonio es un
                                pacto de amor y fidelidad entre un hombre y una mujer, que dura toda la vida, hasta que
                                la muerte los separe. La vida matrimonial implica dejar de ser dos individuos para llegar a
                                ser uno solo, siendo esta relación una imagen de la unión eterna que experimenta la
                                iglesia con Cristo. En la vida familiar, los padres deben criar y discipular a sus hijos con
                                palabra y ejemplo, instruyéndolos y disciplinándolos en el Señor. A su vez los hijos deben
                                obedecer y honrar a sus padres. La vida familiar debe buscar adorar y glorificar en todo a
                                Dios, su Creador.</p>

