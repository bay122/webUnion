                                La Salvación</h5>
                                <p></p>

                                <p class="dropcap text-justify">La salvación es por gracia de Dios, por medio de la fe en Jesús, no por obras, para que
                                nadie se gloríe. El don de la fe en Jesús, es el único medio que Dios dispuso para imputar
                                justicia a su amado pueblo escogido de todas las épocas. Dios estableció un Pacto de
                                Gracia para llevar a cabo su obra salvífica en su pueblo, basándose en la obediencia
                                perfecta de Jesucristo. En su plan de redención, Dios elige de acuerdo al designio de su
                                voluntad, predestinando, llamando, justificando, santificando, preservando y glorificando
                                a sus hijos. Los escogidos responden a la predicación del Evangelio con arrepentimiento y
                                fe, convirtiéndose de sus caminos pecaminosos, para seguir perseverantemente a Jesús,
                                por medio de la obra santificadora del Espíritu Santo.</p>
