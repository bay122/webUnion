                                <p></p>

                                <p class="dropcap text-justify">A través de la creación, podemos entender que existe un Creador, pero solo le conocemos
                                verdaderamente a través de Jesús, por medio de la Biblia y gracias a la iluminación del
                                Espíritu Santo. Esta es su revelación específica, por medio de la cual podemos conocerlo y
                                entender que a pesar de su infinitud, es un Dios cercano.</p>
