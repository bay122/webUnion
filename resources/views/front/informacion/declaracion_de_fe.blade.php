                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                Dios Espíritu Santo</h5>
                                <p></p>

                                <p class="dropcap text-justify">El Espíritu Santo es quien imparte la gracia común a los inconversos y obra la completa
                                redención en los escogidos. Su obra salvífica ha sido igual en todos los tiempos,
                                regenerando los corazones, convenciendo de pecado, santificando y permitiendo
                                perseverar a los hijos de Dios. Su obra capacitadora ministerial fue específica sobre ciertas
                                personas y se amplió a todos los creyentes a partir del día de pentecostés. El Espíritu
                                Santo bautiza a los hijos de Dios al momento de la conversión, viniendo a morar en ellos.</p>
                            </div>

                            <div class="col-md-6 col-sm-12 col-xs-12 sm-margin-five-top">
                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                El Ser Humano y su condición</h5>
                                <p></p>

                                <p class="dropcap text-justify">Dios creó al hombre y a la mujer perfectos según su propia imagen, en plena comunión
                                con Él. Además, estableció en el Edén un Pacto de Obras con el ser humano, requiriendo
                                su completa obediencia, prometiéndoles de esta manera vida eterna. Sin embargo,
                                nuestros padres Adán y Eva, desobedecieron y pecaron contra Dios, lo que trajo como
                                consecuencia la muerte física y espiritual de la humanidad, así como la separación de la
                                gloria de Dios. La imagen de Dios en el hombre, fue letalmente dañada y corrompida,
                                quedando la humanidad eternamente destituida de la presencia de Dios. Desde la caída,
                                el ser humano no quiere, ni puede darle la gloria al Creador, pecando continuamente,
                                siendo merecedores de la ira del Dios Santo y Justo. Toda persona es inexcusable ante El,
                                debido a que Dios se ha dado a conocer a todos por medio de su creación y ha puesto en
                                todos una conciencia moral. No hay persona que pueda salvarse por sus propios méritos
                                de la condenación eterna, necesitando la obra de salvación divina.</p>

