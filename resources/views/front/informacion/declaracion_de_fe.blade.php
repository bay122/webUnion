                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                La Biblia</h5>
                                <p></p>

                                <p class="dropcap text-justify">La Biblia es completamente inspirada por Dios, por tanto verdadera, autoritativa,
                                inerrante e infalible. El mensaje central de la Biblia es cómo Dios se da a conocer por
                                medio de sus palabras y obras, siendo el centro de esa revelación divina Jesucristo mismo.
                                Para enseñar las escrituras se requiere interpretarlas adecuadamente con la guía del
                                Espíritu Santo, siendo necesario entender el contexto histórico y literario de ellas dentro
                                del panorama bíblico general, que siempre indica a Jesús y su Evangelio.</p>
