                                <h5 class=" text-uppercase margin-three-bottom text-mid-gray">
                                Dios Padre</h5>
                                <p></p>

                                <p class="dropcap text-justify">Dios Padre es el Juez Santo y Eterno ante el cual toda persona tendrá que rendir cuentas.
                                El ejerce su misericordia general a la humanidad a través de la gracia común, y su amor
                                específico por su pueblo por medio de la elección y la adopción. La relación que Dios
                                Padre tiene con sus hijos es íntima y especial.</p>
