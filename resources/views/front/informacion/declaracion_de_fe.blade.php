                                <h5 class="text-uppercase margin-three-bottom text-mid-gray">
                                El Evangelio</h5>
                                <p></p>

                                <p class="dropcap text-justify">El centro del mensaje del Cristianismo es el Evangelio. ¿Qué es el Evangelio? Es la buena
                                noticia de salvación y vida eterna que ofrece Dios a través de Jesucristo a todo aquel que
                                cree en Él. El Dios Creador, Santo, Justo y Misericordioso entregó a su Hijo Unigénito
                                Jesús, quien dio su vida en sacrificio por nuestros pecados, para restablecer la comunión
                                con Él y para que podamos disfrutar de Él eternamente. El don supremo del Evangelio es
                                Dios mismo, nuestro máximo bien.
